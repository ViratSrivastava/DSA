package Java.DSA;
class Datatypes
{
    public static void main(String arg[])
    {
        System.out.println("Int min Value:"+Integer.MIN_VALUE);
        System.out.println("Int max Value:"+Integer.MAX_VALUE);
        System.out.println("Int Byte size:"+Integer.BYTES);

        System.out.println("Byte min Value:"+Byte.MIN_VALUE);
        System.out.println("Byte max Value:"+Byte.MAX_VALUE);
        System.out.println("Byte size:"+Byte.BYTES);

        System.out.println("Float min Value:"+Float.MIN_VALUE);
        System.out.println("Float max Value:"+Float.MAX_VALUE);
        System.out.println("Float Byte size:"+Float.BYTES);
        
        System.out.println("Character min Value:"+Character.MIN_VALUE);
        System.out.println("Character max Value:"+Character.MAX_VALUE);
        System.out.println("Character Byte size:"+Character.BYTES);
    }
}
